# Expanding function
# Input the space you want expand, the vector space V where it lives and the subfield Fqk to use to expand
# exp (s in the paper) decides how many expansion perform, verbose print the dimensions.
def expand_subspace(R, V, Fqk, exp=3, verbose=False): 
    R_basis = [Fqn(x.list()) for x in R.basis()]
    R_exp = V.subspace(R_basis) 
    for i in range(exp-1):
        if verbose:
            print(f"Iteration {i+1}: dimension so far = {R_exp.dimension()}")
        z = Fqk.random_element() # Choose a random element in Fqk \ Fq
        while z in Fqk.prime_subfield(): #I should also avoid to use z in the space generated by 1 and previous z, as this is rare I just ignore it.
            z = Fqk.random_element()
        R_z_basis = [z * x for x in R_basis] # Multiply each vector in the original R_basis by z
        R_z = V.subspace(R_z_basis)      
        R_exp = R_exp + R_z  # Add the new subspace to the expanded one   
    if verbose:
        print(f"Dimension after expansion = {R_exp.dimension()}")
    return R_exp

# Contracting function
# Input the space you want contract, the vector space V where it lives and the subfield Fqk to use to contract
# intersect decides how many contractions perform, verbose print the dimensions. 
def contract_subspace(R, V, Fqk, intersect=1, verbose=False):
    R_basis = [Fqn(x.list()) for x in R.basis()]
    R_exp = V.subspace(R_basis) # The space we will contract
    R_exp_basis = [Fqn(x.list()) for x in R_exp.basis()] # Basis of the space we contract
    for i in range(intersect):
        if verbose:
            print(f"Iteration {i+1}: dimension so far = {R_exp.dimension()}")
        z = Fqk.random_element() # Choose a random element in Fqk \ Fq
        while z in Fqk.prime_subfield(): # Avoid trivial intersection.
            z = Fqn.random_element()
        R_exp_z_basis = [z*x for x in R_exp_basis] # Basis of z*R_exp
        R_exp_z = V.subspace(R_exp_z_basis) # Space z*R_exp
        R_exp = R_exp.intersection(R_exp_z) # Update R_exp
        R_exp_basis = [Fqn(x.list()) for x in R_exp.basis()] # Update its basis
    if verbose:
        print(f"Dimension after contraction = {R_exp.dimension()}")
    return R_exp


    
# Expand contract   
def decoding_basic(R,V,Fqk,exp=4,intersect=1,verbose=False):
    R_basis = [Fqn(x.list()) for x in R.basis()]
    R_exp = V.subspace(R_basis) #Initialize the space we will expand.
    R_exp = expand_subspace(R_exp, V, Fqk, exp, verbose)
    R_exp = contract_subspace(R_exp, V, Fqk, intersect, verbose)
    return R_exp
    
# Expand contract fully expand    
def decoding_advance(R,V,Fqk,exp=3,intersect=1,verbose=False):
    R_basis = [Fqn(x.list()) for x in R.basis()]
    R_exp = V.subspace(R_basis) #Initialize the space we will expand.
    R_exp = expand_subspace(R_exp, V, Fqk, exp, verbose)
    R_exp = contract_subspace(R_exp, V, Fqk, intersect, verbose)
    R_exp = expand_subspace(R_exp, V, Fqk, Fqk.degree(), verbose)
    return R_exp



