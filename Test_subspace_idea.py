# RUn test for trials number of trial.
# Each round I generatea random example with a prescribed number of deletions and insertions
# I try to recover the same with 
# alg_1 = the basic algorithm 1 expansion , 1 contraction
# alg_2 = more advance where I do a large expansion and a large contraction
# alg_3 = the most advanced where I do a moderate expansion, followed by a moderate contraction and a full expansion
def test(V, Fqk, deletions, insertions, trials=100):
    counter_alg1= 0 # Algorithm exp = 2, intersect = 1
    counter_alg2= 0 # Algorithm exp = 5, intersect = 7 
    counter_alg3= 0 # Algorithm exp = 3, intersect = 2, exp = k
    for trial in range(trials):
        (R,VA) = corrupt_encode_random(V,Fqk, deletions, insertions, verbose=False) # Encode and corrupt a random element
        s = floor(V.degree()/(2*R.dimension()))
        if decoding_basic(R,V,Fqk,exp=2,intersect=1, verbose=False) == VA: # alg1 successful
            counter_alg1 += 1
        if decoding_basic(R,V,Fqk,exp=s+1,intersect=s+4, verbose=False) == VA: # alg2 successful
            counter_alg2 += 1
        if decoding_advance(R,V,Fqk,exp=s,intersect=2, verbose=False) == VA: # alg3 successful
            counter_alg3 += 1
    print(f"Success rate alg 1 = {float(100*(counter_alg1/trials))}%")    
    print(f"Success rate alg 2 = {float(100*(counter_alg2/trials))}%")  
    print(f"Success rate alg 3 = {float(100*(counter_alg3/trials))}%")
    return [counter_alg1,counter_alg2,counter_alg3]
    


